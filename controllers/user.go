func (uc UserController) GetPostOfUsers(w http.ResponseWriter, r *http.Request, p httprouter.Params) {
	id := p.ByName("id")
	//filters all the posts by userid given so to get all the posts of the given user id
	ux := uc.session.DB("mongo-golang").C("Post").Find(bson.M{"userid": id})
	var FilteredPosts []bson.M
	ux.All(&FilteredPosts)
	uj, err := json.Marshal(FilteredPosts)
	if err != nil {
		fmt.Println(err)
	}
	w.Header().Set("Content-Type", "application/json")
	w.WriteHeader(http.StatusOK)
	fmt.Fprintf(w, "%s\n", uj)
}
